package lesson1_2.variable;

public class Main {

	public static void main(String[] args) {
		// 変数の初期化（宣言と代入を同時に行う）
		int age = 20;

		// ageの中身をコンソールに出力
		System.out.println(age);

		// 変数の中身を上書き
		age = 30;
		System.out.println(age);

		// 練習問題
		// 変数名「height」 値「160.5」
		double height = 160.5;
		// heightの中身をコンソールに出力
		System.out.println(height);
		
		boolean isError = true;
	}
}

package lesson1_2.operator;

public class Ex2 {

	public static void main(String[] args) {
		// 代入演算子
		// 変数の初期化
		int num = 10;
		int total = 0;
		// total = total + num;
		total += num;
		// 結果を表示
		System.out.println(total);

		int num2 = 5;
		// total = total - num2;
		total -= num2;
		// 結果を表示
		System.out.println(total);
	}

}
